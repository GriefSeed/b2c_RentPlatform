package com.soecode.lyf.entity;

public class HeaderItem {
	private int headerItemId;
	private int headerId;
	private int itemId;
	private int attrition;
	private int compens;
	public int getHeaderItemId() {
		return headerItemId;
	}
	public void setHeaderItemId(int headerItemId) {
		this.headerItemId = headerItemId;
	}
	public int getHeaderId() {
		return headerId;
	}
	public void setHeaderId(int headerId) {
		this.headerId = headerId;
	}
	public int getItemId() {
		return itemId;
	}
	public void setItemId(int itemId) {
		this.itemId = itemId;
	}
	public int getAttrition() {
		return attrition;
	}
	public void setAttrition(int attrition) {
		this.attrition = attrition;
	}
	public int getCompens() {
		return compens;
	}
	public void setCompens(int compens) {
		this.compens = compens;
	}
	
	
}

package com.soecode.lyf.entity;

public class Items {
	private int itemsId;
	private String itemsName;
	private int itemsPrice;
	private String itemsImg;
	private int itemsType;
	public int getItemsId() {
		return itemsId;
	}
	public void setItemsId(int itemsId) {
		this.itemsId = itemsId;
	}
	public String getItemsName() {
		return itemsName;
	}
	public void setItemsName(String itemsName) {
		this.itemsName = itemsName;
	}
	
	
	
	public int getItemsPrice() {
		return itemsPrice;
	}
	public void setItemsPrice(int itemsPrice) {
		this.itemsPrice = itemsPrice;
	}
	public String getItemsImg() {
		return itemsImg;
	}
	public void setItemsImg(String itemsImg) {
		this.itemsImg = itemsImg;
	}
	public int getItemsType() {
		return itemsType;
	}
	public void setItemsType(int itemsType) {
		this.itemsType = itemsType;
	}
	
	
}
